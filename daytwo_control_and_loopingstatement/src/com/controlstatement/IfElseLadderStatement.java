package com.controlstatement;

public class IfElseLadderStatement {

	public static void main(String[] args) {
		int mark=75;
		if(mark>=80)
		{
			System.out.println("A Grade");
		}
		else if(mark>=60)
		{
			System.out.println("B Grade");
		}
		else if(mark>=40)
		{
			System.out.println("C Grade");
		}
		else
		{
			System.out.println("Fail");
		}
	}

}
